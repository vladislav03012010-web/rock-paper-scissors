@app.route('/', methods=['GET', 'POST'])
def login():

    if request.method == 'POST':

        name = request.form['name']

        user = User.query.filter_by(name=name).first()

        if not user:
            user = User(name=name)

            db.session.add(user)
            db.session.commit()

            stats = Stats(user_id=user.id)

            db.session.add(stats)
            db.session.commit()

        session['user_id'] = user.id

        return redirect('/game')

    return render_template('login.html')


@app.route('/game', methods=['GET', 'POST'])
def game():

    if 'user_id' not in session:
        return redirect('/')

    mode = request.args.get("mode", "3")

    opts = options[mode]

    result = None
    phrase = ""
    image = ""

    user_choice = None
    comp_choice = None

    stats = Stats.query.filter_by(
        user_id=session['user_id']
    ).first()

    if request.method == 'POST':

        user_choice = request.form['choice']

        comp_choice = random.choice(opts)

        result = get_result(
            user_choice,
            comp_choice,
            opts
        )

        phrase = random.choice(
            phrases[result]
        )

        if result == "win":
            stats.wins += 1
            image = "win.png"

        elif result == "lose":
            stats.losses += 1
            image = "lose.png"

        else:
            stats.draws += 1
            image = "draw.png"

        db.session.commit()

    return render_template(
        "game.html",

        result=result,
        phrase=phrase,
        image=image,

        user=user_choice,
        comp=comp_choice,

        options=opts,
        mode=mode
    )


